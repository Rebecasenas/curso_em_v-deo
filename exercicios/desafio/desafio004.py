# Este programa verifica se o usuário digitou algo que é um número.

# Solicita ao usuário que insira algo e armazena o valor na variável 'a'
a = (input('Digite algo: '))

# Verifica se o valor digitado pelo usuário é numérico usando o método 'isnumeric()' e imprime o tipo do resultado
print(type(a.isnumeric()))